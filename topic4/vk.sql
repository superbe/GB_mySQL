-- Справочник ролей пользователей.
drop table if exists roles;
create table roles (
    id int unsigned not null auto_increment primary key,
    name varchar(255) not null,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp
);

-- Таблица зарегистрированных пользователей.
drop table if exists users;
create table users (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL,
    email_confirmed BIT NOT NULL DEFAULT 0,
    phone VARCHAR(64) NOT NULL,
    phone_confirmed BIT NOT NULL DEFAULT 0,
    password_hash VARCHAR(1024),
    blocked BIT NOT NULL DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- Таблица ролей пользователя.
drop table if exists user_roles;
create table user_roles (
    user_id int unsigned not null,
    role_id int unsigned not null,
    primary key (user_id, role_id),
    foreign key (user_id) references users (id),
    foreign key (role_id) references roles (id)
);

-- Таблица типов медиафайлов.
drop table if exists media_types;
create table media_types (
    id int unsigned not null auto_increment primary key,
    name varchar(255) not null unique,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp
);

-- Таблица медиафайлов.
drop table if exists media;
create table media (
    id int unsigned not null auto_increment primary key,
    media_type_id int unsigned not null,
    user_id int unsigned not null,
    filename varchar(255) not null,
    size int not null,
    metadata JSON,
    hash VARCHAR(1024),
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp,
    foreign key (user_id) references users (id),
    foreign key (media_type_id) references media_types (id)
);

-- Таблица регионов, правильнее использовать либо ОКАТО, либо КЛАДР, по идее можно использовать привязку к геоданным.
drop table if exists regions;
CREATE TABLE regions (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    parent_id INT unsigned DEFAULT NULL,
    code INT DEFAULT NULL,
    zip INT DEFAULT NULL,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp
);

-- Таблица профилей пользователя.
drop table if exists profiles;
create table profiles (
    user_id int unsigned not null primary key,
    first_name varchar(100) not null,
    last_name varchar(100) not null,
    sex char(1) not null,
    birthday date,
    hometown varchar(100),
    photo_id int unsigned not null,
    region_id int unsigned default null,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp,
    foreign key (user_id) references users (id),
    foreign key (region_id) references regions (id),
    foreign key (photo_id) references media (id)
);

-- Таблица сообщений.
drop table if exists messages;
create table messages (
    id int unsigned not null auto_increment primary key, 
    from_user_id int unsigned not null,
    to_user_id int unsigned not null,
    body text not null,
    important boolean,
    delivered boolean,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp,
    foreign key (from_user_id) references users (id),
    foreign key (to_user_id) references users (id)
);

-- Таблица состояний дружбы.
drop table if exists friendship_statuses;
create table friendship_statuses (
    id int unsigned not null auto_increment primary key,
    name varchar(150) not null unique,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp
);

-- Таблица дружбы.
drop table if exists friendship;
create table friendship (
    user_id int unsigned not null,
    friend_id int unsigned not null,
    status_id int unsigned not null,
    requested_at datetime default now(),
    confirmed_at datetime,
    primary key (user_id, friend_id),
    foreign key (user_id) references users (id),
    foreign key (friend_id) references users (id),
    foreign key (status_id) references friendship_statuses (id)
);

-- Таблица групп.
drop table if exists communities;
create table communities (
    id int unsigned not null auto_increment primary key,
    name varchar(150) not null unique,
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp
);

-- Таблица связей пользователей и групп.
drop table if exists communities_users;
create table communities_users (
    community_id int unsigned not null,
    user_id int unsigned not null,
    primary key (community_id, user_id),
    created_at timestamp default current_timestamp,
    updated_at timestamp default current_timestamp on update current_timestamp,
    foreign key (user_id) references users (id),
    foreign key (community_id) references communities (id)
);

-- Таблица типов объектов рейтинга.
